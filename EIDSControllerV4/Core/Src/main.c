void deadTimePWM(uint16_t dtime)
{
	/* Set TIMx_BDTR	->	DTG data	*/
	TIM1->BDTR &= 0xFF00;							//clear	DTG data
	TIM1->BDTR |= (dtime & 0x00FF);	//dtime       	//set	DTG data
}

